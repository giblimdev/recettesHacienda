export function getRecetteBySlug(slug: string): Recette | undefined {
  return recettes.find((r) => r.slug === slug);
}

export function getRecetteById(id: number): Recette | undefined {
  return recettes.find((r) => r.id === id);
}

export function getRecettesByCountry(country: string): Recette[] {
  return recettes.filter((r) => r.country === country);
}

export function getRecettesByCategory(category: string): Recette[] {
  return recettes.filter((r) => r.category === category);
}
